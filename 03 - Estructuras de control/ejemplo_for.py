'''
Ejemplos de uso del bucle for
'''

# Contar 4 veces
for n in range(4):
    print("Hola")

# Contar desde un inicio hasta un final
for n in range(10, 21):
    print(n)
for n in range(20, 9, -2):
    print(n)

# Recorrer los elementos de una lista
numeros = [2, 5, 7, 8]
for num in numeros:
    print(num)